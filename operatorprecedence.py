#Operator precedence describes the order in which the operations are performed.
# Operators of a higher precedence are always executed first.
#examples
result = 3*4+5
print(result)

result_two = 3*(4+5)
print(result_two)

result_three = 3*4+5-1
print(result_three)

result_four = 3*(4+5)-1
print(result_four)

#What would be the output of the following expressions;
result_five = 5*3**2
print(result_five)

result_six = (5+3)*2**2-10/2
print(result_six)

result_seven = 25/5+2*1
print(result_seven)

result_eight = (25/5)+2*1
print(result_eight)



